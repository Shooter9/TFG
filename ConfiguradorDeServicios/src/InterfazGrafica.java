    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
        BasicDBObject document = new BasicDBObject();
        document.put("_id", new ObjectId(idDelete.getText()));
        tablaServicios.remove(document);
        JOptionPane.showMessageDialog(null, "Servicio eliminado correctamente");
        idDelete.setText("");
        }catch(RuntimeException err  ){
        JOptionPane.showMessageDialog(null, "No se encuentra el servicio especificado");
        idDelete.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

