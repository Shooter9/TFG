    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (!serviceName.getText().equals("") && !uri_dir.getText().equals("") && !status.getText().equals("")
                && !description.getText().equals("") && !in_param.getText().equals("") && !output.getText().equals("")) {
            
            BasicDBObject documentAdd = new BasicDBObject();
            documentAdd.put("serviceName", "" + serviceName.getText() + "");
            documentAdd.put("URI", "" + uri_dir.getText() + "");
            documentAdd.put("status", "" + status.getText() + "");
            documentAdd.put("description", "" + description.getText() + "");
            documentAdd.put("input_param", "" + in_param.getText() + "");
            documentAdd.put("output", "" + output.getText() + "");

            tablaServicios.insert(documentAdd);
            JOptionPane.showMessageDialog(null, "Servicio agregado correctamente");
            serviceName.setText("");
            uri_dir.setText("");
            status.setText("");
            description.setText("");
            in_param.setText("");
            output.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Para agregar un servicio todos los campos deben estar informados.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        MostrarServiciosTextAreas.setText("");
        DBCursor cursor = tablaServicios.find();
        if(cursor.hasNext()){
        while (cursor.hasNext()) {
           DBObject obj = cursor.next();            
           MostrarServiciosTextAreas.setText(MostrarServiciosTextAreas.getText()+"\n"+obj);
        }
        }else{
           JOptionPane.showMessageDialog(null, "Actualmente no hay ning√∫n servicio registrado");
        }
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

